package cn.twt.entity;

/**
 * 项目表
 */
public class Bugproject {
    private Integer id;    //项目编号
    private String name;   //项目名称

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
